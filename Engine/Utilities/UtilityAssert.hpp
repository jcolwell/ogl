#include <Windows.h>

#define LOG(format, ...)\
	{\
		{\
			char buffer[1024];\
			sprintf_s(buffer, 1024, (#format), __VA_ARGS__);\
			std::string message;\
			message += (buffer);\
			message += "\n";\
			OutputDebugStringA(message.c_str());\
		}\
	}

//----------------------------------------------------------------------------------

#define ASSERT(condition, format, ...)\
	{\
		if (!(condition))\
		{\
			LOG(format, __VA_ARGS__)\
			DebugBreak();\
		}\
	}

